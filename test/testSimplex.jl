function testsplx(data)
    posOR_B, dirR_B, rCoM = data
    My = rcsMixMatrix(posOR_B, dirR_B, [], rCoM)
    # Random.seed!(13085610)
    N = size(My, 2)
    maxErr = -1.0
    r = RcsAllocator(My)
    uerr = [];
    yerr = []
    for i in 1:100000
        y = rand(N)
        # y[5] = 1.0
        # y[9] = 1.0
        # y[20] = 1.0
        u = My * y
        rcsAllocationSimplex!(r, u)
        yr = r.y
        if minimum(yr) < 0.0 || maximum(yr) > 1.0
            @show "err"
        end
        ur = My*yr
        maxErr = max(maxErr, norm(ur - u))
        if norm(ur - u) > 1e-8
            @show ur, u, i
            # @show norm(ur - u)
            @show ("nope")
            push!(yerr, y)
            push!(uerr, u)
        end
    end
    return maxErr
end

function testsplxAlloc(data)
    posOR_B, dirR_B, rCoM = data
    My = rcsMixMatrix(posOR_B, dirR_B, [], rCoM)
    N = size(My, 2)
    r = RcsAllocator(My)
    y = rand(N)
    u = My * y
    @btime rcsAllocationSimplex!($r, $u)
    return nothing
end

